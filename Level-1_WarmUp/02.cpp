// Write a program to read two numbers and print their sum.

#include <iostream>
using namespace std;

int main () {
    int a, b;

    cout << "Enter value of a: ";
    cin >> a;

    cout << "Enter value of b: ";
    cin >> b;

    cout << endl << "Sum of " << a << " & " << b << " is: " << a + b << endl;
}